package com.txbox.settings.bean;

public class ClipBean {
	String duration;
	String clipsize;
	String clipparam;
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public String getClipsize() {
		return clipsize;
	}
	public void setClipsize(String clipsize) {
		this.clipsize = clipsize;
	}
	public String getClipparam() {
		return clipparam;
	}
	public void setClipparam(String clippara) {
		this.clipparam = clippara;
	}
	
}
